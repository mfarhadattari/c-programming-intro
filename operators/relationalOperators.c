#include <stdio.h>

void main()
{
    int num1, num2;
    num1 = 10;
    num2 = 13;
    // equal
    printf("%d \n", num1 == num2);

    // not equal
    printf("%d \n", num1 != num2);

    // grater then
    printf("%d \n", num1 > num2);

    // grater then or equal
    printf("%d \n", num1 >= num2);

    // less then
    printf("%d \n", num1 < num2);

    // les then or equal
    printf("%d \n", num1 <= num2);
}